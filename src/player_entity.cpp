#include "player_entity.hpp"

PlayerEntity::PlayerEntity(glm::vec3 pos, glm::vec3 size, Texture2D texture, Shader shader)
{
    this->Position = pos;
    this->Size = size;
    this->Rotation = 0.0f;
    this->texture = texture;
    this->shader = shader;

    this->initRenderData();
}

PlayerEntity::~PlayerEntity()
{
    glDeleteVertexArrays(1, &this->VAO);
}

void PlayerEntity::Draw()
{
    // Prepare transformations
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, this->Position);

    model = glm::translate(model, glm::vec3(0.5f * this->Size.x, 0.5f * this->Size.y, 0.5f * this->Size.z));
    model = glm::rotate(model, this->Rotation, glm::vec3(0.0f, 1.0f, 0.0f));
    model = glm::translate(model, glm::vec3(-0.5f * this->Size.x, -0.5f * this->Size.y, -0.5f * this->Size.y));

    model = glm::scale(model, this->Size);

    this->shader.Use();
    this->shader.SetInteger("entity", true);
    this->shader.SetMatrix4("model", model);

    glActiveTexture(GL_TEXTURE0);
    this->texture.Bind();

    glBindVertexArray(this->VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    this->shader.SetInteger("entity", false);
}

