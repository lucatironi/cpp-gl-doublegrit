#include "player_entity.hpp"

PlayerEntity::PlayerEntity(glm::vec3 position, glm::vec3 size, Texture2D texture, Shader shader) :
    Position(position),
    size(size),
    rotation(0.0f),
    acceleration(glm::vec3(0.0f)),
    velocity(glm::vec3(0.0f)),
    texture(texture),
    shader(shader)

{
   initRenderData();
}

PlayerEntity::~PlayerEntity()
{
    glDeleteVertexArrays(1, &VAO);
}

// Processes input received from any keyboard-like input system. Accepts input parameter in the form of camera defined ENUM (to abstract it from windowing systems)
void PlayerEntity::ProcessKeyboard(Player_Movement direction)
{
    if (direction == PLAYER_FORWARD)
       acceleration.z = -PLAYER_ACCELERATION;
    if (direction == PLAYER_BACKWARD)
       acceleration.z = PLAYER_ACCELERATION;
    if (direction == PLAYER_LEFT)
       acceleration.x = -PLAYER_ACCELERATION;
    if (direction == PLAYER_RIGHT)
        acceleration.x = PLAYER_ACCELERATION;
    if (direction == PLAYER_STOPZ)
        acceleration.z = 0.0f;
    if (direction == PLAYER_STOPX)
        acceleration.x = 0.0f;
}

void PlayerEntity::Update(GLfloat deltaTime)
{
    velocity.x += acceleration.x * deltaTime - velocity.x * std::min(PLAYER_FRICTION * deltaTime, 1.0f);
    velocity.z += acceleration.z * deltaTime - velocity.z * std::min(PLAYER_FRICTION * deltaTime, 1.0f);

    // position
    Position.x += velocity.x * deltaTime;
    Position.z += velocity.z * deltaTime;
}

void PlayerEntity::Draw()
{
    // Prepare transformations
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, Position);

    model = glm::translate(model, glm::vec3(0.5f * size.x, 0.5f * size.y, 0.5f * size.z));
    model = glm::rotate(model, rotation, glm::vec3(0.0f, 1.0f, 0.0f));
    model = glm::translate(model, size * -0.5f);

    model = glm::scale(model, size);

    shader.Use();
    shader.SetInteger("entity", true);
    shader.SetMatrix4("model", model);

    glActiveTexture(GL_TEXTURE0);
    texture.Bind();

    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    shader.SetInteger("entity", false);
}

